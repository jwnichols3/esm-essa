package com.bgi.essa.jboss4.xml;

import java.util.ArrayList;
import java.util.List;

/**
 * Container for parsed tasks
 * 
 * @author coleguy
 */
public class ConfigurationParseTree {
	
	/**
	 * Return collected tasks.
	 * 
	 * @return collected tasks.
	 */
	public List<TaskTree> getTaskTree() {
		return(_task_list);
	}
	
	/**
	 * Add new task to tree
	 * 
	 * @param arg fresh task for tree
	 */
	public void addTaskTree(TaskTree arg) {
		_task_list.add(arg);
	}

	private List<TaskTree> _task_list = new ArrayList<TaskTree>();
}
