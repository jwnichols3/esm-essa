public class Forums {
    int usersId;
    String usersList;
    int devId;
    int cvsId;

    public int getUsersId() {
        return usersId;
    }

    public void setUsersId(int usersId) {
        this.usersId = usersId;
    }

    public String getUsersList() {
        return usersList;
    }

    public void setUsersList(String usersList) {
        this.usersList = usersList;
    }

    public int getDevId() {
        return devId;
    }

    public void setDevId(int devId) {
        this.devId = devId;
    }

    public int getCvsId() {
        return cvsId;
    }

    public void setCvsId(int cvsId) {
        this.cvsId = cvsId;
    }
}
